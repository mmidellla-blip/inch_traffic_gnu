							<h1 class="tit">판결경정신청의 가능여부</h1>
							<div class="qna-wrap">
								<h1><span>Q</span>근저당권말소를 청구하는 소송을 제기해 승소를 했습니다. 그런데 판결문을 받고 보니 등기부등본 상의 상대방의 주소와 판결문 상의 상대방의 주소가 다르게 기재되어 있습니다. 이럴 경우 판결경정신청을 해야 하나요?</h1>
								<p><span>A</span>아닙니다. 판례는 이와 같은 경우는 판결경정사유에 해당하지 않는다고 판시하고 있습니다(대법원 1994.8.16. 자 94그17 결정). <br><br>
								이는 등기소에 판결서를 가지고 근저당권말소를 청구하면 판결서 및 등기부등본에 기재되어 있는 당사자의 주민등록번호를 보고 동일인임이 증명되어 주소가 다르더라도 등기를 해주기 때문으로 보입니다. <br><br>
								그렇지 않다 하더라도 등기 당사자가 동일인임을 증명할 수 있는 자료를 제출하면 등기가 가능합니다. 그러므로 판결경정신청을 할 필요는 없습니다.</p>
							</div>
						</section>
						<section class="business-detail-box">
							<h1 class="tit">소송의 종결사유</h1>
							<h1 class="sub-tit"><span>종국판결</span></h1>
							<p>"종국판결"이란 소송 또는 상소의 제기에 따라 소송이 진행된 사건의 전부 또는 일부를 현재 계속하고 있는 심급에서 완결시키는 판결을 말합니다[「법률용어사전」(대검찰청)].</p>
							<ul>
								<li>판결은 소송이 제기된 날부터 5개월 이내에 선고해야 합니다(「민사소송법」 제199조 본문).</li>
							</ul>
							<h1 class="sub-tit"><span>청구의 포기, 인낙</span></h1>
							<p>개념</p>
							<ul>
								<li>"청구의 포기"란 원고가 변론 또는 변론준비기일에 소송물인 권리관계의 존부에 대한 자기 주장을 부정하고 그것이 이유없다는 것을 자인하는 법원에 대한 소송상의 진술을 말합니다[「법률용어사전」(대검찰청)].</li>
								<li> "청구의 인낙"이란 피고가 권리관계의 유무에 관한 원고의 주장을 이유있다고 인정하는 법원에 대한 진술을 말합니다[「법률용어사전」(대검찰청)].</li>
							</ul>
							<p>효력</p>
							<ul>
								<li>청구의 포기·인낙을 변론조서·변론준비기일조서에 적은 경우 그 조서는 확정판결과 같은 효력을 가집니다(「민사소송법」 제220조).</li>
							</ul>
							<h1 class="sub-tit"><span>화해권고결정</span></h1>
							<ul class="no-bullet">
								<li>"화해권고결정"이란 법원·수명법관 또는 수탁판사가 소송이 진행 중인 사건에 대해 직권으로 당사자의 이익, 그 밖의 모든 사정을 참작하여 청구의 취지에 어긋나지 않는 범위 안에서 사건의 공평한 해결을 위해 화해를 권고하는 결정을 말합니다(「민사소송법」 제225조).</li>
								<li>효력</li>
							</ul>
							<ul>
								<li>화해권고결정은 다음 중 어느 하나에 해당하면 확정판결과 같은 효력을 가집니다(「민사소송법」 제231조 및 제220조).</li>
								<li>결정서의 정본을 송달받은 날부터 2주 이내에 이의신청이 없는 경우(「민사소송법」 제226조제1항).</li>
								<li>이의신청에 대한 각하결정이 확정된 경우</li>
								<li>당사자가 이의신청을 취하하거나 이의신청권을 포기한 경우</li>
							</ul>
							<h1 class="sub-tit"><span>소장각하명령</span></h1>
							<ul class="no-bullet">
								<li>소장에 흠이 있어 보정명령을 내렸음에도 기간 이내에 흠을 보정하지 않은 경우 재판장은 명령으로 소장을 각하해야 합니다(「민사소송법」 제254조제1항 및 제2항).</li>
								<li>소장각하명령에 대해서는 즉시항고 할 수 있습니다(「민사소송법」 제254조제3항).</li>
							</ul>
							<h1 class="sub-tit"><span>소송의 취하</span></h1>
							<p>소송이 제기된 후 원고가 법원에 소송의 전부 또는 일부를 철회하는 소송행위를 말합니다[「법률용어사전」(대검찰청)].</p>
							<ul>
								<li> 소송을 취하하면 소송은 당초 제기하지 않은 것과 동일한 상태로 돌아가게 됩니다[「법률용어사전」(대검찰청)].</li>
							</ul>
						</section>
						<section class="business-detail-box">
							<h1 class="tit">판결의 종류</h1>
							<h1 class="sub-tit"><span>종국판결</span></h1>
							<p>종국판결</p>
							<ul>
								<li>"전부판결"이란 소송계속이 되고 있는 사건의 전부를 동시에 재판하는 종국판결을 말합니다[「법률용어사전」(대검찰청)].</li>
							</ul>
							<p>일부판결</p>
							<ul>
								<li>"일부판결"이란 소송사건의 일부를 다른 부분과 분리해 독립적으로 재판할 수 있는 상태에 이르렀을 때 그 부분만을 재판하는 종국판결을 말합니다(「민사소송법」 제200조). <br>예를 들어 본소와 반소가 진행되던 중 본소만을 먼저 판결하는 경우를 말합니다.</li>
							</ul>
							<p>추가판결</p>
							<ul>
								<li>"추가판결"이란 법원이 청구의 일부에 대해 재판을 누락한 경우 그 청구부분에 대해서만 재판하는 종국판결을 말합니다(「민사소송법」 제212조).</li>
								<li>판결은 법원의 판단을 분명하게 하기 위해 결론을 주문에 기재하도록 되어 있으므로 재판의 누락이 있는지 여부는 우선 주문의 기재에 의해 판단해야 합니다(대법원 2002. 5. 14. 선고 2001다73572 판결).</li>
							</ul>
							<h1 class="sub-tit"><span>중간판결</span></h1>
							<ul class="no-bullet">
								<li>- "중간판결"이란 소송의 진행 중 문제가 되었던 실체상 또는 소송상의 각 쟁점을 미리 판단하고 해결하여 종국판결을 준비하기 위해 행하는 판결을 말합니다[「법률용어사전」(대검찰청)].</li>
								<li> - 법원은 다음의 해당 부분에 대해 중간판결을 할 수 있습니다(「민사소송법」 제201조).</li>
							</ul>
							<ul>
								<li>독립된 공격 또는 방어의 방법 <br>※ 예를 들어 소유권확인소송에서 소유권취득원인에 대해 주장하는 것은 독립된 공격방법으로 먼저 소유권취득원인에 대한 판결을 할 수 있습니다.</li>
								<li> 그 밖의 중간의 다툼에 대해 필요한 경우 <br>※ 예를 들어 원고는 소송의 당사자가 아니라거나, 관할이 잘못되었다는 등과 같이 소송요건에 대한 다툼이 있을 경우 이를 먼저 판단하는 것을 말합니다.</li>
