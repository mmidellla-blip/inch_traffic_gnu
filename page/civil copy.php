							<h1 class="tit">재판장의 소장심사 및 보정명령</h1>
							<h1 class="sub-tit"><span>소장심사 대상</span></h1>
							<p>소장의 기재사항</p>
							<ul>
								<li>소장에는 당사자와 법정대리인, 청구의 취지와 원인을 적어야 합니다(「민사소송법」 제249조제1항).</li>
							</ul>
							<p>보정명령</p>
							<ul>
								<li>재판장은 소장심사를 한 후 다음과 같은 경우에는 상당한 기간을 정하고, 그 기간 이내에 흠을 보정하도록 명령합니다(「민사소송법」 제254조제1항·제4항). <br>소장에 기재사항이 제대로 기재되어 있지 않은 경우(「민사소송법」 제249조제1항) <br>소장에 법률의 규정에 따른 인지를 붙이지 않은 경우 <br>소장에 인용한 서증(書證)의 등본 또는 사본을 붙이지 않은 경우</li>
								<li>재판장은 필요하다고 인정하는 경우 원고가 청구하는 이유에 대응하는 증거방법을 구체적으로 적어 내도록 명할 수 있습니다(「민사소송법」 제254조제4항).</li>
							</ul>
							<h1 class="sub-tit"><span>소장의 각하</span></h1>
							<ul class="no-bullet">
								<li>원고가 정해진 기간 이내에 흠을 보정하지 않은 경우 재판장은 명령으로 소장을 각하합니다(「민사소송법」 제254조제2항).</li>
								<li>각하명령에 대해서는 즉시항고를 할 수 있습니다(「민사소송법」 제254조제3항).</li>
