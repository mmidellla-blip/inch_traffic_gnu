							<h1 class="tit">소장의 제출</h1>
							<h1 class="sub-tit"><span>소 제기</span></h1>
							<p>소송은 법원에 소장을 제출함으로써 제기합니다(「민사소송법」 제248조).</p>
							<h1 class="sub-tit"><span>관할</span></h1>
							<p>보통재판적 소재지</p>
							<ul>
								<li>소송은 피고의 보통재판적(普通裁判籍)이 있는 곳의 법원이 관할합니다(「민사소송법」 제2조).</li>
								<li>사람 : 피고의 주소지 또는 거소지(「민사소송법」 제3조)</li>
								<li>대사(大使)·공사(公使), 그 밖에 외국의 재판권 행사대상에서 제외되는 대한민국 국민이 주소지 또는 거소지가 없는 경우: 대법원이 있는 곳[「민사소송법」 제4조]</li>
								<li>법인, 그 밖의 사단 또는 재단: 주된 사무소 또는 영업소 소재지(만약 사무소와 영업소가 없는 경우에는 주된 업무담당자의 주소)[「민사소송법」 제5조]</li>
								<li>국가: 해당 건과 관련해 국가를 대표하는 관청 또는 대법원이 있는 곳(「민사소송법」 제6조)</li>
							</ul>
							<p>특별재판적 소재지</p>
							<ul>
								<li>「민사소송법」은 특별한 경우 보통재판적이 없는 곳에서도 소송을 진행할 수 있도록 특별재판적을 규정하고 있습니다.</li>
								<li>사무소 또는 영업소에 계속해서 근무하는 사람에 대해 소송을 제기하는 경우: 그 사무소 또는 영업소가 있는 곳을 관할하는 법원(「민사소송법」 제7조)</li>
								<li>재산권에 관한 소송을 제기하는 경우: 거소지 또는 의무이행지의 법원(「민사소송법」 제8조)</li>
								<li>어음·수표에 관한 소송을 제기하는 경우: 지급지의 법원(「민사소송법」 제9조)</li>
								<li>선원에게 재산권에 관한 소송을 제기하는 경우: 선적(船籍)이 있는 곳의 법원(「민사소송법」 제10조제1항)</li>
								<li>군인·군무원에게 재산권에 관한 소송을 제기하는 경우: 군사용 청사가 있는 곳 또는 군용 선박의 선적이 있는 곳의 법원(「민사소송법」 제10조제2항)</li>
								<li>대한민국에 주소가 없는 사람 또는 주소를 알 수 없는 사람에게 재산권에 관한 소송을 제기하는 경우: 청구의 목적 또는 담보의 목적이나 압류할 수 있는 피고의 재산이 있는 곳의 법원(「민사소송법」 제11조)</li>
								<li>사무소 또는 영업소가 있는 사람에게 그 사무소 또는 영업소의 업무와 관련이 있는 소송을 제기하는 경우: 그 사무소 또는 영업소가 있는 곳의 법원(「민사소송법」 제12조)</li>
								<li>선박 또는 항해에 관한 일로 선박소유자, 그 밖의 선박이용자에게 소송을 제기하는 경우: 선적이 있는 곳의 법원(「민사소송법」 제13조)</li>
								<li>선박채권(船舶債權), 그 밖에 선박을 담보로 한 채권에 관한 소송을 제기하는 경우: 선박이 있는 곳의 법원(「민사소송법」 제14조)</li>
								<li>회사, 그 밖의 사단이 사원에게 소송을 제기하는 경우: 그 소송이 사원의 자격으로 말미암은 것이면 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제15조제1항)</li>
								<li>사원이 다른 사원에게 소송을 제기하는 경우: 그 소송이 사원의 자격으로 말미암은 것이면 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제15조제1항)</li>
								<li>사단 또는 재단이 그 임원에게 소송을 제기하는 경우: 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제15조제2항)</li>
								<li>회사가 그 발기인 또는 검사인에게 소송을 제기하는 경우: 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제15조제2항)</li>
								<li>회사, 그 밖의 사단의 채권자가 그 사원에게 소송을 제기하는 경우: 그 소송이 사원의 자격으로 말미암은 것이면 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제16조 및 제15조)</li>
								<li>회사, 그 밖의 사단, 재단, 사원 또는 사단의 채권자가 그 사원·임원·발기인 또는 검사인이었던 사람에게 소송을 제기하는 경우: 그 소송이 사원의 자격으로 말미암은 것이면 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제17조 , 제15조 및 제16조)</li>
								<li>사원이었던 사람이 그 사원에게 소송을 제기하는 경우: 그 소송이 사원의 자격으로 말미암은 것이면 회사, 그 밖의 사단의 보통재판적이 있는 곳의 법원(「민사소송법」 제17조 , 제15조 및 제16조)</li>
