								<h1><span>Q</span>저는 부동산 매입 후 소유권이전등기만을 남겨놓은 상황이었는데, 매도자가 그만 사고로 사망했습니다. 상속인들은 망자와 저와의 매매계약을 인정하지 않으며 소유권이전등기를 해주지 않기에 소송을 제기하게 되었습니다. 그런데 제가 아는 주소는 망자의 주소 뿐이고 상속인들의 주소는 알지 못하는 상황입니다. 연락도 잘 안 됩니다. 소장 부본이 송달이 안 되어서 주소보정명령을 받았는데 어떻게 해야 하나요?</h1>
								<p><span>A</span>주소보정명령서를 가지고 동사무소를 방문해 상속인들의 주소를 알아보시기 바랍니다. 법원의 명령서가 있을 경우에는 동사무소에서 주민등록등본 및 초본을 발급해 줍니다. <br><br>발급받은 주민등록등본 등을 가지고 주소보정서를 제출하시기 바랍니다. <br><br>만약 주소보정서를 제출했음에도 송달이 안 되어 다시 재송달, 특별송달 등의 과정을 거쳤음에도 불구하고 상속인을 찾을 수 없다면 공시송달을 신청해 보시기 바랍니다. <br><br>공시송달은 본인이 할 수 있는 모든 방법을 동원했음에도 상대방에게 송달이 되지 않은 경우에 신청할 수 있는 최후의 방법으로 법원이 허가하면 법원게시판 게시·관보·공보 또는 신문 게재·전자통신매체를 이용한 공시를 거쳐 송달이 인정되는 제도입니다.</p>
