							<h1 class="tit">답변서 제출</h1>
							<h1 class="sub-tit"><span>답변서 제출통보</span></h1>
							<p>법원은 소장의 부본을 송달할 때에 피고가 원고의 청구를 다투는 경우에는 답변서를 제출하라는 취지를 피고에게 알립니다(「민사소송법」 제256조제2항 및 제1항).</p>
							<h1 class="sub-tit"><span>답변서의 작성</span></h1>
							<p>답변서에는 다음의 사항을 적어야 합니다(「민사소송법」 제256조제4항, 제274조제1항, 제2항 및 「민사소송규칙」 제65조제1항).</p>
							<ul>
								<li>당사자의 성명·명칭 또는 상호와 주소 </li>
								<li>대리인의 성명과 주소</li>
								<li>사건의 표시</li>
								<li>공격 또는 방어의 방법 : 주장을 증명하기 위한 증거방법에 대한 의견 기재</li>
								<li>상대방의 청구와 공격 또는 방어의 방법에 대한 진술 : 상대방의 증거방법에 대한 의견 기재</li>
								<li>덧붙인 서류의 표시</li>
								<li>작성한 날짜</li>
								<li>법원의 표시</li>
								<li>청구 취지에 대한 답변</li>
								<li>소장에 기재된 개개의 사실에 대한 인정 여부 및 증거방법</li>
								<li>항변과 이를 뒷받침하는 구체적 사실 및 증거방법</li>
							</ul>
							<p>첨부서류</p>
							<ul>
								<li>답변서에는 증거방법 중 입증이 필요한 사실에 관한 중요한 서증의 사본을 첨부해야 합니다(「민사소송규칙」 제65조제2항).</li>
								<li>당사자가 가지고 있는 문서로 답변서에 인용한 것은 그 등본 또는 사본을 붙여야 합니다(「민사소송법」 제256조제4항 및 제275조제1항).</li>
								<li>문서의 일부가 필요한 경우에는 그 부분에 대한 초본을 붙이고, 문서가 많을 때에는 그 문서를 표시하면 됩니다(「민사소송법」 제256조제4항 및 제275조제2항).</li>
								<li>첨부서류는 상대방이 요구하면 그 원본을 보여 주어야 합니다(「민사소송법」 제256조제4항 및 제275조제3항).</li>
							</ul>
							<h1 class="sub-tit"><span>답변서 제출기한</span></h1>
							<p>피고는 소장의 부본을 송달받은 날부터 30일 이내에 답변서를 제출해야 합니다(「민사소송법」 제256조제1항 본문).</p>
							<ul>
								<li>다만, 피고가 공시송달의 방법에 따라 소장의 부본을 송달받은 경우에는 그렇지 않습니다(「민사소송법」 제256조제1항 단서).</li>
							</ul>
							<h1 class="sub-tit"><span>보정명령</span></h1>
							<p>재판장은 답변서의 기재사항 등이 제대로 기재되어 있지 않거나 중요한 서증의 사본이 첨부되지 않은 때에는 법원서기관·법원사무관·법원주사 또는 법원주사보로 하여금 방식에 맞는 답변서의 제출을 촉구하게 할 수 있습니다(「민사소송규칙」 제65조제3항).</p>
							<h1 class="sub-tit"><span>답변서의 송달</span></h1>
							<p>법원은 답변서의 부본을 원고에게 송달합니다(「민사소송법」 제256조제3항).</p>
							<h1 class="sub-tit"><span>답변서 미제출의 효과</span></h1>
							<p>법원은 피고가 답변서를 제출하지 않은 경우 청구의 원인이 된 사실을 자백한 것으로 보고 변론 없이 판결할 수 있습니다(「민사소송법」 제257조제1항 본문).</p>
							<ul>
								<li>다만, 직권으로 조사할 사항이 있거나 판결이 선고되기까지 피고가 원고의 청구를 다투는 취지의 답변서를 제출한 경우에는 그렇지 않습니다(「민사소송법」 제257조제1항 단서).</li>
							</ul>
							<p>자백하는 취지의 답변서 제출의 경우</p>
							<ul>
								<li>피고가 청구의 원인이 된 사실을 모두 자백하는 취지의 답변서를 제출하고 따로 항변을 하지 않은 경우 법원은 변론 없이 판결할 수 있습니다(「민사소송법」 제257조제2항 및 제1항).</li>
							</ul>
							<p>선고 기일 통지</p>
							<ul>
								<li>법원은 피고에게 소장의 부본을 송달할 때에 변론 없이 판결을 선고할 기일을 함께 통지할 수 있습니다(「민사소송법」 제257조제3항).</li>
							</ul>
						</section>
