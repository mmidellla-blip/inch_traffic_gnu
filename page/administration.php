							<h1 class="tit"><span>행정소송 상호간의 관계</span></h1>
							<h1 class="sub-tit"><span>행정소송 상호간의 관계</span></h1>
							<p>취소소송과 무효확인소송의 관계</p>
							<ul>
								<li>취소소송과 무효확인소송은 별개의 독립한 소송형태입니다. 그러므로 행정청의 처분 등에 불복하는 사람은 제소요건을 충족하는 한, 목적을 가장 효과적으로 달성할 수 있는 항고소송 유형을 선택할 수 있습니다.</li>
								<li>취소소송과 무효확인소송은 이론상 별개의 소송이기는 하지만 실제로는 영역이 겹치므로 당사자가 처분취소의 소를 제기한 경우에도 그 처분에 무효사유가 있다고 판단하면, 처분을 취소하는 원고 전부승소의 판결(무효를 선언하는 의미의 취소판결)을 해야 하고, 반대로 처분 무효확인을 구하는 소를 제기했으나 그 처분에 단지 취소사유만 있고 취소소송의 제기에 필요한 소송요건을 갖추었다고 판단할 때에는 당사자에게 취소의 소로 청구취지를 변경하도록 한 후, 취소의 판결을 해야 합니다.</li>
							</ul>
							<p>취소소송·무효확인소송과 부작위위법확인소송의 관계</p>
							<ul>
								<li>부작위위법확인소송은 처분취소소송·무효확인소송에 대한 관계에서 보충적인 지위에 있습니다. 따라서 처분이 존재하거나 존재하는 것으로 의제되는 경우에는 부작위위법확인소송은 소의 이익이 없어 부적합합니다.</li>
								<li>예를 들어 구 「정보공개법」(2013. 8. 6. 법률 제11991호로 개정되기 전의 것) 제11조제5항과 같이 작위의무의 이행기한을 지키지 않으면 거부처분이 있는 것으로 의제하는 특별규정이 있는 경우 거부처분취소소송을 제기하지 않고 부작위위법확인소송을 고집한다면 부적법하므로 각하를 면할 수 없습니다.</li>
							</ul>
							<h1 class="sub-tit"><span>항고소송과 당사자소송의 관계</span></h1>
							<p>취소소송과 당사자소송의 관계</p>
							<ul>
								<li>행정처분은 비록 하자가 있더라도 당연 무효가 아닌 한 공정력이 있어 공적 기관이 취소할 때까지는 일단 유효한 것으로 취급하므로, 행정처분에 취소사유에 해당하는 흠이 있는 경우, 취소소송 이외의 방법으로는 그 효력을 부인할 수 없습니다.</li>
								<li> 예를 들어 위법한 과세처분에 따라 세금을 납부한 사람도 그 과세처분이 당연 무효가 아닌 이상, 과세처분 취소소송을 제기해야하며 취소소송을 제기함이 없이 납부한 세금의 반환을 구하는 소송을 제기할 수 없습니다.</li>
							</ul>
							<p>무효확인소송과 당사자소송의 관계</p>
							<ul>
								<li>처분이 무효인 경우에는 공정력이 없어 누구나 그 효력 없음을 전제로 당사자소송을 제기할 수 있습니다. </li>
								<li>예를 들어 공무원 파면처분이 무효인 경우 항고소송으로서 파면처분 무효확인의 소가 가능할 뿐만 아니라 당사자소송으로서 파면 이후 복직시까지의 급여지급을 구하는 소송도 제기할 수 있습니다.</li>
							</ul>
