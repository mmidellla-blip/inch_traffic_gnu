								즉, 위법한 처분에 따라 발생한 위법상태를 배제하여 원상으로 회복시키고, 그 처분으로 침해되거나 방해받은 권리와 이익을 보호·구제하려고 하는 소송입니다(대법원 1992. 4. 24. 선고 91누11131 판결).</li>
								<li>행정처분은 위법이라도 사실상 통용되는 효력을 가지고 있기 때문에 소송에 의해 그 처분의 효력을 다투기 위해서는 취소소송을 제기해야 합니다. <br>
								취소소송은 처분 등의 취소를 구할 법률상 이익이 있는 사람이 처분 등을 행한 행정청을 피고로 하여 피고의 소재지를 관할하는 행정법원에 제소기간 내에 제기하면 됩니다.</li>
							</ul>
							<p>무효등확인소송</p>
							<ul>
								<li>무효등확인소송은 행정청의 처분이나 재결의 효력유무 또는 그 존재여부를 확인하는 소송입니다(「행정소송법」 제4조제2호). <br>
								무효등확인소송의 전형적인 것은 처분 등의 무효확인소송이지만 처분의 존부의 확인을 구하는 처분의 존재 또는 부존재확인소송 등이 있습니다.</li>
								<li>행정처분의 위법성이 중대하고 명백하여 당연무효인 경우 그 처분은 효력을 갖지 않기 때문에 국민으로서는 그 처분에 대해 소송을 제기할 필요도 없고 구속을 받지도 않습니다. 그러나 이러한 경우에도 해당처분이 무효임을 확인받을 필요가 있습니다. <br>이러한 필요성을 충족시켜주는 소송형식이 무효등확인소송이고 이 소송은 사전에 행정심판을 거칠 필요도 없고, 제소기간의 제한을 받지도 않습니다(「행정소송법」 제38조).</li>
								<li>따라서 처분 등의 효력 유무 또는 존재여부의 확인을 구할 법률상 이익이 있는 사람은 언제든지 처분 등을 행한 행정청을 피고로 하여 피고의 소재지를 관할하는 행정법원에 무효등확인소송을 제기할 수 있습니다.</li>
							</ul>
