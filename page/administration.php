								<li>사정재결이란 심판청구가 이유 있다고 인정되는 경우에도 이를 인용하는 것이 공공복리에 크게 위배될 때에 그 심판청구를 기각하는 재결을 말합니다(「행정심판법」 제44조제1항).</li>
								<li>예를 들어, 구청장의 甲에 대한 단란주점영업허가신청에 대한 거부처분이 위법하다고 해도 구청장에게 단란주점영업허가를 하도록 하거나 행정심판위원회가 직접 단란주점영업허가를 하는 것이 공공복리에 적합하지 않은 경우에는 甲의 청구를 기각할 수도 있습니다.</li>
							</ul>
							<p>취소심판의 인용재결이 있게 되면 원처분은 처음부터 없는 상태로 됩니다. 즉, 그 재결의 형성력에 의하여 해당 처분은 별도의 행정처분을 기다릴 것 없이 당연히 취소되어 소멸됩니다(대법원 1998. 4.24. 선고 97누17131 판결).</p>
						</section>
